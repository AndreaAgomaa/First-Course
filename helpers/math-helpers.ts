export function addTwoNumbers(number1: number, number2: number = 0) {
  return number1 + number2;
}

export function substractTwoNumbers(number1: number, number2: number = 0) {
  return number1 - number2;
}

export function divideTwoNumbers(number1: number, number2: number = 0) {
  return number1 / number2;
}

export function timesTwoNumbers(number1: number, number2: number = 0) {
  return number1 * number2;
}
